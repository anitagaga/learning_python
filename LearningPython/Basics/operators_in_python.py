# Arithmetic operators
x = 15
y = 4

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)
print(x // y)

# Assignment operators
x1 = 10
x1 = 10 + 2
print(x1)
x1 += 2
print(x1)
x1 -= 2
print(x1)

# Comparison operators
x2 = 10
y2 = 6

print(x2 == y2)
print(x2 != y2)
print(x2 > y2)
print(x2 < y2)
print(x2 >= y2)
print(x2 <= y2)

# Logical operators
x3 = 10
y3 = 4

print(x3 == y3 and x3 > y3)
print(x3 == y3 or x3 > y3)

# Identity operators
x4 = ["anita", "software"]
y4 = ["anita", "software"]

print(x4 is y4)
print(x4 == y4)
print(x4 is not y4)

# Membership operators
x5 = ["anita", "software"]
y5 = ["anita", "software"]

print("anita" in y5)
print("anita" not in y5)

# Bitwise operators
# & - AND
# | - OR
# ~  NOT
# ^  XOR

x6 = 1
y6 = 3
print(x6 & y6)
print(x6 | y6)
